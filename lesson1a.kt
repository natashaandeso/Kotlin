fun main(){
    //  A variable is a container that stores data/values
    // In kotlin we have two main types of initializers i.e var and val
    // Val => immutable (unchangeable) but var => mutable (changeable)

    val name = "Natasha" // string -> a series of character inside of quotes
    val number1 = 5 // Interger -> whole number
    val number2 = 40.6 // Double -> Number with a decimal
    var letter = "k" // Char -> It is a single letter of the alphabet
    val isRegistered = true // Boolean -> It evaluates to either true or false


    println(name)
    println(number1)
    println(number2)
    println(letter)
    println(isRegistered)
    
}